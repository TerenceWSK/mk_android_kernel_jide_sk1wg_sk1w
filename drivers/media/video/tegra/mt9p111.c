static int sensor_write_reg8(struct i2c_client *client, u16 addr, u8 val)
{
	int err;
	struct i2c_msg msg;
	unsigned char data[3];
	int retry = 0;

	if (!client->adapter)
		return -ENODEV;

	data[0] = (u8) (addr >> 8);
	data[1] = (u8) (addr & 0xff);
	data[2] = (u8) (val & 0xff);

	msg.addr = client->addr;
	msg.flags = 0;
	msg.len = 3;
	msg.buf = data;

	do {
		err = i2c_transfer(client->adapter, &msg, 1);
		if (err == 1)
			return 0;
		retry++;
		pr_err("mt9p111 : i2c transfer failed, retrying %x %x %d\n",
		       addr, val, err);
		msleep(3);
	} while (retry <= SENSOR_MAX_RETRIES);

	return err;
}

static int sensor_write_reg16(struct i2c_client *client, u16 addr, u16 val)
{
	int err;
	struct i2c_msg msg;
	unsigned char data[4];
	int retry = 0;

	if (!client->adapter)
		return -ENODEV;

	data[0] = (u8) (addr >> 8);
	data[1] = (u8) (addr & 0xff);
        data[2] = (u8) (val >> 8);
	data[3] = (u8) (val & 0xff);

	msg.addr = client->addr;
	msg.flags = 0;
	msg.len = 4;
	msg.buf = data;

	do {
		err = i2c_transfer(client->adapter, &msg, 1);
		if (err == 1)
			return 0;
		retry++;
		pr_err("mt9p111 : i2c transfer failed, retrying %x %x %d\n",
		       addr, val, err);
		msleep(3);
	} while (retry <= SENSOR_MAX_RETRIES);

	return err;
}
static int sensor_write_table(struct i2c_client *client,
			      const struct sensor_reg table[])
{
	const struct sensor_reg *next;
	int err;
    
	pr_info("mt9p111 %s \n",__func__);
    
    next = table ;       

	for (next = table; next->op != SENSOR_TABLE_END; next++) {

		switch (next->op) {
		case WRITE_REG_DATA8:
		{
	        //pr_info("write reg data 8 \n");
			err = sensor_write_reg8(client, next->addr,
				next->val);
			if (err)
				return err;
			break;
		}
		case WRITE_REG_DATA16:
		{
	        //pr_info("write reg data 16 \n");
			err = sensor_write_reg16(client, next->addr,
				next->val);
			if (err)
				return err;
			break;
		}
		case SENSOR_WAIT_MS:
		{
			msleep(next->val);
			break;
		}
		default:
			pr_err("%s: invalid operation 0x%x\n", __func__,
				next->op);
			return err;
		}
	}
	return 0;
}

static int get_Lens_Shading(struct i2c_client *client)
{
    int err,i;
    u16 first_reg = 0x0c08;
    u16 m_uReg=0x0000,m_uValue=0x0000;
    all_lens_shadings[0].op = WRITE_REG_DATA16;
    all_lens_shadings[0].addr = 0x3210;
    all_lens_shadings[0].val = 0x49B0;
    for(i = 2;i<=MAX_LENS-2;i++)
    {
    	all_lens_shadings[i-1].op = WRITE_REG_DATA16;
	err = eeprom_read_reg(client, first_reg, &all_lens_shadings[i-1].addr);
	if (err)
      		return err;
	
	err = eeprom_read_reg(client, first_reg+2, &all_lens_shadings[i-1].val);

	if (m_uReg == 0xffff && m_uValue == 0xffff) 
	{
		pr_err("%s : from  m_uReg =0x%04x ,val = 0x%04x to read end!\n",__func__,m_uReg,m_uValue);
		err = NO_LENS_SHADING;
		return err;
	}
	
	first_reg += 4;
    } 
	all_lens_shadings[MAX_LENS-2].op = WRITE_REG_DATA16;
    	all_lens_shadings[MAX_LENS-2].addr = 0x3210;
    	all_lens_shadings[MAX_LENS-2].val = 0x49B8;
	
	all_lens_shadings[MAX_LENS-1].op = SENSOR_TABLE_END;
	all_lens_shadings[MAX_LENS-1].addr=all_lens_shadings[MAX_LENS-1].val= 0x0000;
	
	if (err)
		return err;

	return 0;
}
